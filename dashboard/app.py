def make_line_chart(df_plot, y_col, title, color):
    fig = go.Figure()
    x = df_plot["timestamp"] if "timestamp" in df_plot.columns else df_plot.index
    fig.add_trace(go.Scatter(
        x=x, y=df_plot[y_col],
        mode="lines",
        line=dict(color=color, width=1.6),
        fill="tozeroy",
        fillcolor=f"{color}0d",
        name=y_col,
    ))
    fig.update_layout(**PLOT_LAYOUT, title=dict(text=title, font=dict(size=12, color="#94a3b8")))
    return fig


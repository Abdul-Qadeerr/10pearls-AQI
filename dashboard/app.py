current_file = Path(__file__).resolve()
project_root = current_file.parent.parent

CSV_PATHS = [
    project_root / "kandhkot_aqi_data.csv",
    project_root / "data" / "kandhkot_aqi_data.csv",
    project_root / "feature_pipeline" / "kandhkot_aqi_data.csv",
    current_file.parent / "kandhkot_aqi_data.csv",
]


def load_csv_data():
    for path in CSV_PATHS:
        if path.exists():
            try:
                return pd.read_csv(path), path.name
            except Exception:
                pass
    return None, None


def generate_fallback_data(n: int = 120) -> pd.DataFrame:
    dates = pd.date_range(end=pd.Timestamp.now(), periods=n, freq="H")
    np.random.seed(42)
    base = 120 + 30 * np.sin(np.linspace(0, 4 * np.pi, n))
    return pd.DataFrame({
        "timestamp":   dates,
        "aqi":         np.clip(base + np.random.normal(0, 8, n), 50, 220).round(1),
        "temperature": np.random.uniform(28, 44, n).round(1),
        "humidity":    np.random.uniform(35, 72, n).round(1),
        "pm25":        np.random.uniform(35, 90, n).round(1),
        "pm10":        np.random.uniform(50, 130, n).round(1),
        "wind_speed":  np.random.uniform(1.5, 9.0, n).round(2),
    })



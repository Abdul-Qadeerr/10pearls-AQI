def classify_aqi(value):
    try:
        value = float(value)
    except (TypeError, ValueError):
        return "Unknown", "#64748b"
    if value <= 50:   return "Good",                              "#22c55e"
    if value <= 100:  return "Moderate",                          "#eab308"
    if value <= 150:  return "Unhealthy for Sensitive Groups",    "#f97316"
    if value <= 200:  return "Unhealthy",                         "#ef4444"
    if value <= 300:  return "Very Unhealthy",                    "#a855f7"
    return                   "Hazardous",                         "#991b1b"



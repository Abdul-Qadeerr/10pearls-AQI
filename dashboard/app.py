@st.cache_data(ttl=3600, show_spinner=False)
def load_forecast():
    """
    Attempts to load forecast from the Hopsworks-backed inference module.
    Falls back to a simple linear extrapolation if the model is not available.
    """
    try:
        import sys
        sys.path.insert(0, str(project_root))
        from inference.predict import predict
        return predict(), "model"
    except Exception as e:
        return None, str(e)


